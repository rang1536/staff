package study.db;

public class Staff {
	private int staffNo;
	private String staffName;
	private String staffSn;
	private String Graduateday;
	private int schoolNo;
	private int religionNo;
	
	public int getStaffNo() {
		return staffNo;
	}
	public void setStaffNo(int staffNo) {
		this.staffNo = staffNo;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getStaffSn() {
		return staffSn;
	}
	public void setStaffSn(String staffSn) {
		this.staffSn = staffSn;
	}
	public String getGraduateday() {
		return Graduateday;
	}
	public void setGraduateday(String graduateday) {
		Graduateday = graduateday;
	}
	public int getSchoolNo() {
		return schoolNo;
	}
	public void setSchoolNo(int schoolNo) {
		this.schoolNo = schoolNo;
	}
	public int getReligionNo() {
		return religionNo;
	}
	public void setReligionNo(int religionNo) {
		this.religionNo = religionNo;
	}
	
	

}
